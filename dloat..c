#include<stdio.h>
void main()
{
    float num1;
    double num2;

    printf("\nEnter a float number:");
    scanf("%f",&num1);


    printf("Enter a double number:");
    scanf("lf", &num2);

    printf("\nThe entered float number is :%f",num1);

    printf("\nThe entered double number is :%lf",num2);

}
